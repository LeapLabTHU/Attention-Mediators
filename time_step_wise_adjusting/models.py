class SwitchModelThree(nn.Module):
    def __init__(self, model1, model2, model3, switch_ratio1, switch_ratio2,
                 first_several_num=10, distence_func=F.mse_loss, save_dir=None):
        super().__init__()
        self.init_diff = None
        self.model1 = model1
        self.model2 = model2
        self.model3 = model3
        self.switch_ratio1 = switch_ratio1
        self.switch_ratio2 = switch_ratio2
        self.use_model1 = True
        self.use_model2 = False
        self.use_model3 = False
        self.first_several_num = first_several_num
        self.distence_func = distence_func
        self.save_dir = save_dir

    def switch_to_model1(self):
        self.use_model1 = True
        self.use_model2 = False
        self.use_model3 = False

    def switch_to_model2(self):
        self.use_model1 = False
        self.use_model2 = True
        self.use_model3 = False

    def switch_to_model3(self):
        self.use_model1 = False
        self.use_model2 = False
        self.use_model3 = True

    def forward(self, x, t, y):
        if self.use_model1:
            return self.model1(x, t, y)
        elif self.use_model2:
            return self.model2(x, t, y)
        elif self.use_model3:
            return self.model3(x, t, y)

    def forward_with_cfg(self, x, t, y, cfg_scale):
        if self.use_model1:
            return self.model1.forward_with_cfg(x, t, y, cfg_scale)
        elif self.use_model2:
            return self.model2.forward_with_cfg(x, t, y, cfg_scale)
        elif self.use_model3:
            return self.model3.forward_with_cfg(x, t, y, cfg_scale)


